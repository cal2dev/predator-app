<?php

namespace Entities;

/**
 * AppLoginData
 */
class AppLoginData
{
    /**
     * @var integer
     */
    private $logdId;

    /**
     * @var integer
     */
    private $dataId;

    /**
     * @var string
     */
    private $regRecordhash;

    /**
     * @var string
     */
    private $logdCookieHash;

    /**
     * @var string
     */
    private $sessId;


    /**
     * Get logdId
     *
     * @return integer
     */
    public function getLogdId()
    {
        return $this->logdId;
    }

    /**
     * Set dataId
     *
     * @param integer $dataId
     *
     * @return AppLoginData
     */
    public function setDataId($dataId)
    {
        $this->dataId = $dataId;

        return $this;
    }

    /**
     * Get dataId
     *
     * @return integer
     */
    public function getDataId()
    {
        return $this->dataId;
    }

    /**
     * Set regRecordhash
     *
     * @param string $regRecordhash
     *
     * @return AppLoginData
     */
    public function setRegRecordhash($regRecordhash)
    {
        $this->regRecordhash = $regRecordhash;

        return $this;
    }

    /**
     * Get regRecordhash
     *
     * @return string
     */
    public function getRegRecordhash()
    {
        return $this->regRecordhash;
    }

    /**
     * Set logdCookieHash
     *
     * @param string $logdCookieHash
     *
     * @return AppLoginData
     */
    public function setLogdCookieHash($logdCookieHash)
    {
        $this->logdCookieHash = $logdCookieHash;

        return $this;
    }

    /**
     * Get logdCookieHash
     *
     * @return string
     */
    public function getLogdCookieHash()
    {
        return $this->logdCookieHash;
    }

    /**
     * Set sessId
     *
     * @param string $sessId
     *
     * @return AppLoginData
     */
    public function setSessId($sessId)
    {
        $this->sessId = $sessId;

        return $this;
    }

    /**
     * Get sessId
     *
     * @return string
     */
    public function getSessId()
    {
        return $this->sessId;
    }
}
